namespace CalculatorStudents.Operations
{
    public class CalcOperationsAllMethods
    {

        public double Add(double NumberAd, double NumberBd)
        {
            return NumberAd + NumberBd;
        }

        public double Division(double NumberAd, double NumberBd)
        {
            return NumberAd / NumberBd;
        }

        public double Multiply(double NumberAd, double NumberBd)
        {
            return NumberAd * NumberBd;
        }

        public double Subtract(double NumberAd, double NumberBd)
        {
            return NumberAd - NumberBd;
        }
    }
}